from heapq import heappush, heappop 

def shortestBridge(grid):    
    ## find the first 1
    for i in range(len(grid)):
        for j in range(len(grid[0])):
            # print(i, j, grid[i][j])
            if grid[i][j] == 1: 
                start = (i, j)
                break 
        if grid[i][j] == 1: 
            break 
    
    queue = [(0, start)]
    visited = set([start])
    # print(visited)
    # add neighbors to the queue; if the 
    # print("start", start)
    while queue: 
        dist, coords= heappop(queue)
        u, v = coords
        if dist > 0 and grid[u][v] == 1: 
            # print("end: ", u,v, "dist: ", dist)
            return dist
        # visited.add((u,v))
        for p, q in [(u+1, v), (u-1, v), (u, v+1), (u, v-1)]:
            if 0 <= p < len(grid) and 0 <= q < len(grid[0]) and (p,q) not in visited:
                # print(p, q, grid[p][q], dist)
                if grid[p][q] == 1:
                    if dist == 0:
                        heappush(queue, (0, (p, q)))
                    else: 
                        heappush(queue, (dist, (p, q)))
                else: 
                    heappush(queue, (dist + 1, (p, q)))
                visited.add((p,q))
    return -1



